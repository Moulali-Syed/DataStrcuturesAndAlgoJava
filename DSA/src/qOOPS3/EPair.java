package qOOPS3;

public class EPair {

	private int first;
	private int second;
	
	public EPair(int first,int second) {
		this.first = first;
		this.second = second;
	}
	
	public int getFirst() {
		return first;
	}
	
	public int getSecond() {
		return second;
	}
	
	public void setFirst(int first) {
		this.first = first;
	}
	
	public void setSecond(int second) {
		this.second  = second;
	}
}
